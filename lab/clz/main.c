#include <stdio.h>
#include <stdint.h>

extern int clz(uint64_t);

void bin(uint64_t n) 
{ 
    for (uint64_t i = 1ULL << 63; i > 0; i = i >> 1) 
        (n & i)? printf("1"): printf("0"); 
} 

void test(uint64_t x){
    bin(x);
    printf("  ----clz--->  %d\n",clz(x));
}

int main(){
    test(0b0000000000000000000011111101101010101010101001010101010100000000);
    test(0b0001000000000000000011111101101010101010101001010101010100000000);
    test(1);
    test(0);
    test(0b0000000000000001000000000000000000000000000000000000000000000000);
    test(0b0000000000000000100000000000000000000000000000000000000000000000);
    test(0b0000000000000000000000000000000010000000000000000000000000000000);
    test(0b0000000000000000000000000000000000000000001000000000000000000000);
    test(0x0000000000f22cc0); // 40
    test(0xFFFFFFFFFFFFFFFF);
    test(0x0000000000010000);
    return 0;
}
